fig.savefig(
    exceedance_figure_path,
    bbox_inches="tight",
)

print(f"Exceedance map saved to: {exceedance_figure_path}")


# %% Count threshold exceedances over land and ocean

summary_threshold = 0.9
summary_origin = "noaa"
summary_years = [1990, 2000, 2010, 2020, 2026]

summary_lat, summary_lon = load_coordinates(summary_origin)

summary_result_dir = RESULTS_DIR / summary_origin
summary_path = summary_result_dir / (
    f"saves_prob_g_0_{summary_origin}_{start_year}.txt"
)

if not summary_path.exists():
    raise FileNotFoundError(
        f"Interpolated probability file was not found: "
        f"{summary_path}"
    )

with summary_path.open("r", encoding="utf-8") as file:
    summary_fields = np.asarray(
        json.load(file),
        dtype=float,
    )

lon_grid, lat_grid = np.meshgrid(
    summary_lon,
    summary_lat,
)

land_mask = globe.is_land(lat_grid, lon_grid)

for end_year in summary_years:
    period_index = end_years.index(end_year)
    field = summary_fields[period_index]

    valid_mask = np.isfinite(field)
    land_valid = land_mask & valid_mask
    ocean_valid = ~land_mask & valid_mask

    all_exceeding = (field > summary_threshold) & valid_mask
    land_exceeding = (field > summary_threshold) & land_valid
    ocean_exceeding = (field > summary_threshold) & ocean_valid

    print(f"Timespan: {start_year}-{end_year}")

    print(
        f"All grid cells exceeding {summary_threshold}: "
        f"{np.sum(all_exceeding)}/{np.sum(valid_mask)}"
    )

    print(
        f"Ocean grid cells exceeding {summary_threshold}: "
        f"{np.sum(ocean_exceeding)}/{np.sum(ocean_valid)}"
    )

    print(
        f"Land grid cells exceeding {summary_threshold}: "
        f"{np.sum(land_exceeding)}/{np.sum(land_valid)}"
    )

