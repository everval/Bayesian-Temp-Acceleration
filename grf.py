"""
Interpolate and visualise spatial posterior estimates of temperature acceleration.

This script:
1. loads posterior summaries generated by data_grid.py;
2. identifies grid cells with missing posterior estimates;
3. interpolates missing spatial values using a Gaussian random field;
4. generates maps of posterior acceleration and exceedance probability;
5. determines when probability thresholds are first exceeded;
6. summarises threshold exceedance over land and ocean.

Run data_grid.py before running this script.
"""
