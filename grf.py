def grf(a):
    """
    Interpolate missing grid-cell values using a Gaussian random field.

    A Matérn covariance model is fitted to the observed grid cells. Conditional
    expectations under the fitted model are then used to fill missing cells.

    Parameters
    ----------
    values : numpy.ndarray
        Two-dimensional spatial field containing observed values and NaNs.

    Returns
    -------
    numpy.ndarray
        Spatial field with missing values replaced by conditional GRF means.

    Notes
    -----
    Spatial locations are represented by grid indices rather than physical
    distances in latitude and longitude.
    """
    def pos(n, m):
        i,j = np.indices((n,m))
        return np.stack((i,j),axis=-1)
    x_known = a.flatten()
    s_known = pos(a.shape[0],a.shape[1]).reshape(-1, 2)
    keep = ~np.isnan(x_known)
    x_known = x_known[keep];s_known = s_known[keep]
    s_unknown = pos(a.shape[0],a.shape[1]).reshape(-1, 2)
    s_unknown = s_unknown[~keep]
    
    def gen_cov_mat(s,sig,nu,l):
        n = s.shape[0]
        cov_matrix = np.zeros((n,n))
        h = s[:, np.newaxis, :] - s[np.newaxis, :, :]
        h = np.linalg.norm(h,axis=-1)
        np.fill_diagonal(h,10)
        cov_matrix = sig**2*(2**(1-nu)/gamma(nu))*((h/l)**nu)*kv(nu,h/l)
        np.fill_diagonal(cov_matrix,sig**2)
        return cov_matrix
    
    def mean(s,mu):
        n = s.shape[0]
        return mu*np.ones(n)
    
    def log_like_simp(params,sig,x,s):
        nu,l = params
        cov_mat = gen_cov_mat(s,sig,nu,l)
        L = np.linalg.cholesky(cov_mat)
        n = x.shape[0]
        L_inv = np.linalg.solve(L,np.eye(n))
        cov_inv = L_inv.T@L_inv
        log_det_cov = 2*np.sum(np.log(np.diag(L)))
        mu = np.sum(cov_inv@x)/np.sum(cov_inv)*np.ones(n)
        return n*np.log((x-mu).T@cov_inv@(x-mu))+log_det_cov
    
    def callback(xk):
        print("Evaluating at:", xk)
    nu,l = minimize(log_like_simp,[1,1],method='SLSQP',args=(1,x_known,s_known),bounds=[(1e-6,2),(1e-6,50)],callback=callback).x
    mu = np.sum(gen_cov_mat(s_known,1,nu,l)@x_known)/np.sum(gen_cov_mat(s_known,1,nu,l))
    sig = 1/len(x_known)*x_known.T@np.linalg.solve(gen_cov_mat(s_known,1,nu,l),np.eye(s_known.shape[0]))@x_known
    
    def pred(x_known,s_known,s_unknown,mu,sig,nu,l):
        s = np.concatenate((s_unknown,s_known))
        n = s_known.shape[0]
        d = s_unknown.shape[0]
        full_cov = gen_cov_mat(s,sig,nu,l)
        cov_un = full_cov[:d,:d]
        cov_kn = full_cov[d:,d:]
        cov_un_kn = full_cov[:d,d:]
        cov_kn_inv = np.linalg.solve(cov_kn,np.eye(n))
        E_un = mean(s_unknown,mu)+cov_un_kn@cov_kn_inv@(x_known-mean(s_known,mu))
        cond_cov = cov_un - cov_un_kn@cov_kn_inv@cov_un_kn.T
        return E_un,np.diag(cond_cov)
    
    x_unknown,cov_unknown = pred(x_known,s_known,s_unknown,mu,sig,nu,l)
    x = np.zeros(len(x_unknown)+len(x_known));x[keep] = x_known;x[~keep] = x_unknown
    return x.reshape(a.shape)

# %% Helper function for loading dataset coordinates
def load_coordinates(data_origin):
    """
    Load the latitude and longitude coordinates for a climate dataset.

    Parameters
    ----------
    data_origin : str
        Dataset identifier. Supported values are "hadcrut", "noaa",
        "berkeley", and "era5".

    Returns
    -------
    tuple of numpy.ndarray
        Latitude and longitude coordinates.
    """
    data_path = DATA_DIR / f"data_grid_{data_origin}.nc"

    if not data_path.exists():
        raise FileNotFoundError(
            f"Input dataset was not found: {data_path}"
        )

    with nc.Dataset(str(data_path), mode="r") as fname:
        if data_origin == "hadcrut":
            lat = fname.variables["latitude"][:].data
            lon = fname.variables["longitude"][:].data

        elif data_origin == "noaa":
            lat = fname.variables["lat"][:].data
            lon = fname.variables["lon"][:].data - 180

        elif data_origin == "berkeley":
            lat = (
                fname.variables["latitude"][:]
                .data.reshape(180 // 5, 5)
                .mean(axis=1)
            )
            lon = (
                fname.variables["longitude"][:]
                .data.reshape(360 // 5, 5)
                .mean(axis=1)
            )

        elif data_origin == "era5":
            raw_lat = fname.variables["latitude"][:].data
            raw_lon = fname.variables["longitude"][:].data

            # Downscale the original ERA5 grid to 36 latitude bands.
            lat = np.flipud(
                np.mean(
                    [
                        raw_lat[i * 21 - i : (i + 1) * 21 - i]
                        for i in range(36)
                    ],
                    axis=1,
                )
            )

            # Downscale the original ERA5 grid to 72 longitude bands.
            lon = (
                np.mean(
                    [
                        raw_lon[1:][i * 19 + i : (i + 1) * 19 + i]
                        for i in range(72)
                    ],
                    axis=1,
                )
                - 180
            )

        else:
            raise ValueError(
                "data_origin must be 'hadcrut', 'noaa', "
                "'berkeley', or 'era5'."
            )

    return np.asarray(lat), np.asarray(lon)


