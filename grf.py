# Dataset used for interpolation and inspection.
data_origin = "era5"

# Quantity extracted from the posterior result files:
# "prob_g_0" contains posterior sign probabilities.
# "post_means" contains posterior parameter means.
dataset = "prob_g_0"

start_year = 1970

# The end year is exclusive. For example, 2026 contains observations
# through the end of 2025.
end_years = [
    1990,
    1995,
    2000,
    2005,
    2010,
    2015,
    2020,
    2026,
]

if dataset not in {"prob_g_0", "post_means"}:
    raise ValueError(
        "dataset must be either 'prob_g_0' or 'post_means'."
    )

lat, lon = load_coordinates(data_origin)

result_dir = RESULTS_DIR / data_origin
result_dir.mkdir(parents=True, exist_ok=True)


# %% Load posterior acceleration estimates

coeff = np.full(
    (len(end_years), len(lat), len(lon)),
    np.nan,
    dtype=float,
)

for period_index, end_year in enumerate(end_years):
    input_path = result_dir / (
        f"{dataset}_{data_origin}_{start_year}_{end_year}.txt"
    )

    if not input_path.exists():
        raise FileNotFoundError(
            f"Posterior result file was not found: {input_path}"
        )

    with input_path.open("r", encoding="utf-8") as file:
        posts = json.load(file)

    for lat_index, latitude in enumerate(lat):
        for lon_index, longitude in enumerate(lon):
            coordinate = f"{latitude} {longitude}"
            posterior_values = posts.get(coordinate)

            if (
                posterior_values is not None
                and len(posterior_values) > 2
                and posterior_values[2] is not None
            ):
                # The third posterior entry is the quadratic coefficient a_2,
                # interpreted as the local acceleration parameter.
                coeff[
                    period_index,
                    lat_index,
                    lon_index,
                ] = posterior_values[2]


# Use a consistent spatial support across all truncation periods.
# A grid cell is treated as missing in every period if it is unavailable
# in at least one period.
available_in_all_periods = np.all(np.isfinite(coeff), axis=0)
coeff[:, ~available_in_all_periods] = np.nan

for end_year, field in zip(end_years, coeff):
    missing_count = np.isnan(field).sum()

    print(
        f"{data_origin.upper()} {start_year}-{end_year}: "
        f"{missing_count} grid cells require interpolation"
    )


