# %% Interpolate missing grid cells and save results

interpolated_coeff = np.empty_like(coeff)

for period_index, field in enumerate(coeff):
    if np.isnan(field).any():
        # Use the conditional GRF mean to interpolate grid cells for which
        # the temporal model could not be estimated.
        interpolated_coeff[period_index] = grf(field)
    else:
        interpolated_coeff[period_index] = field.copy()

save_path = result_dir / (
    f"saves_{dataset}_{data_origin}_{start_year}.txt"
)

with save_path.open("w", encoding="utf-8") as file:
    json.dump(interpolated_coeff.tolist(), file)

print(f"Interpolated fields saved to: {save_path}")


